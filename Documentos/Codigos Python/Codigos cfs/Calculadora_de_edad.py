# Calculadora de edad

edad = int(input("Ingresa tu edad: "))
dias = edad * 365
semanas = edad * 52
meses = edad * 12
horas = dias * 24
minutos = horas * 60
segundos = minutos * 60
print("Usted a vivido ", dias, "dias", semanas, "semanas", meses, "meses", horas, "horas", minutos, "minutos", segundos, "segundos.")